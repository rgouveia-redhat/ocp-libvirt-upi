#!/bin/bash

if ! ( [ -f ./.re-run-with-network ] || [ -f Settings ] ); then
    echo "Error: No Settings or network config foun. Was the cluster created?"
    exit -1
fi

source Settings

network_prefix=$(cat .re-run-with-network)
#echo $network_prefix

# Backup current HAProxy config.
if [ -f /etc/haproxy/haproxy.cfg ]; then
    sudo cp /etc/haproxy/haproxy.cfg /etc/haproxy/haproxy.cfg-$(/usr/bin/date +%N)
fi

echo "## Generated by $(pwd) $0 ##

#---------------------------------------------------------------------
# Example configuration for a possible web application.  See the
# full configuration options online.
#
#   https://www.haproxy.org/download/1.8/doc/configuration.txt
#
#---------------------------------------------------------------------

#---------------------------------------------------------------------
# Global settings
#---------------------------------------------------------------------
global
    # to have these messages end up in /var/log/haproxy.log you will
    # need to:
    #
    # 1) configure syslog to accept network log events.  This is done
    #    by adding the '-r' option to the SYSLOGD_OPTIONS in
    #    /etc/sysconfig/syslog
    #
    # 2) configure local2 events to go to the /var/log/haproxy.log
    #   file. A line like the following can be added to
    #   /etc/sysconfig/syslog
    #
    #    local2.*                       /var/log/haproxy.log
    #
    log         127.0.0.1 local2

    chroot      /var/lib/haproxy
    pidfile     /var/run/haproxy.pid
    maxconn     4000
    user        haproxy
    group       haproxy
    daemon

    # turn on stats unix socket
    stats socket /var/lib/haproxy/stats

    # utilize system-wide crypto-policies
    ssl-default-bind-ciphers PROFILE=SYSTEM
    ssl-default-server-ciphers PROFILE=SYSTEM

#---------------------------------------------------------------------
# common defaults that all the 'listen' and 'backend' sections will
# use if not designated in their block
#---------------------------------------------------------------------
defaults
    mode                    http
    log                     global
    option                  httplog
    option                  dontlognull
    option http-server-close
    option forwardfor       except 127.0.0.0/8
    option                  redispatch
    retries                 3
    timeout http-request    10s
    timeout queue           1m
    timeout connect         10s
    timeout client          1m
    timeout server          1m
    timeout http-keep-alive 10s
    timeout check           10s
    maxconn                 3000

#---------------------------------------------------------------------
# OpenShift Cluster
#---------------------------------------------------------------------
" | sudo tee /etc/haproxy/haproxy.cfg


if [ "$INSTALLATION_PLATFORM" == "none" ]; then

    echo "# External LB

listen api-server-6443
  bind *:6443
  mode tcp
  server lb ${network_prefix}.3:6443 check inter 1s

listen ingress-router-443
  bind *:443
  mode tcp
  balance source
  server lb ${network_prefix}.3:443 check inter 1s

listen ingress-router-80
  bind *:80
  mode tcp
  balance source
  server lb ${network_prefix}.3:80 check inter 1s

" | sudo tee /etc/haproxy/haproxy.cfg

else # there are only two cases?

    # TODO: Check if workers exist. Ingress must point to them.

    sudo echo "# Self-managed VIP

listen api-server-6443
  bind *:6443
  mode tcp
  server lb ${network_prefix}.4:6443 check inter 1s

listen ingress-router-443
  bind *:443
  mode tcp
  balance source
  server lb ${network_prefix}.5:443 check inter 1s

listen ingress-router-80
  bind *:80
  mode tcp
  balance source
  server lb ${network_prefix}.5:80 check inter 1s

" >> /etc/haproxy/haproxy.cfg

fi

sudo systemctl restart haproxy.service
sleep 2
systemctl status haproxy.service
